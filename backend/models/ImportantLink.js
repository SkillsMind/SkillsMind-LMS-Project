const mongoose = require('mongoose');

const importantLinkSchema = new mongoose.Schema({
  title: {
    type: String,
    required: [true, 'Title is required'],
    trim: true,
    maxlength: [200, 'Title cannot exceed 200 characters']
  },
  url: {
    type: String,
    required: [true, 'URL is required'],
    trim: true
  },
  description: {
    type: String,
    trim: true,
    maxlength: [500, 'Description cannot exceed 500 characters']
  },
  course: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'Course',
    required: [true, 'Course is required']
  },
  category: {
    type: String,
    enum: ['Study Material', 'Reference', 'Tool', 'Other'],
    default: 'Study Material'
  },
  createdBy: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  isActive: {
    type: Boolean,
    default: true
  }
}, {
  timestamps: true
});

// Index for faster queries
importantLinkSchema.index({ course: 1, category: 1 });
importantLinkSchema.index({ isActive: 1 });

module.exports = mongoose.model('ImportantLink', importantLinkSchema);